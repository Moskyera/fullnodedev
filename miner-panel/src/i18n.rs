#[derive(Clone, Copy, PartialEq, Eq)]
pub enum Lang {
    En,
    El,
    Tr,
    Zh,
    Ja,
    Es,
    Fr,
    Th,
    Ru,
}

impl Lang {
    pub const ALL: [Lang; 9] = [
        Lang::En,
        Lang::El,
        Lang::Tr,
        Lang::Zh,
        Lang::Ja,
        Lang::Es,
        Lang::Fr,
        Lang::Th,
        Lang::Ru,
    ];

    pub fn name(self) -> &'static str {
        match self {
            Lang::En => "English",
            Lang::El => "Ελληνικά",
            Lang::Tr => "Türkçe",
            Lang::Zh => "中文",
            Lang::Ja => "日本語",
            Lang::Es => "Español",
            Lang::Fr => "Français",
            Lang::Th => "ไทย",
            Lang::Ru => "Русский",
        }
    }

    pub fn from_code(s: &str) -> Lang {
        match s.trim().to_lowercase().as_str() {
            "el" | "gr" => Lang::El,
            "tr" => Lang::Tr,
            "zh" | "cn" => Lang::Zh,
            "ja" | "jp" => Lang::Ja,
            "es" => Lang::Es,
            "fr" => Lang::Fr,
            "th" => Lang::Th,
            "ru" => Lang::Ru,
            _ => Lang::En,
        }
    }

    pub fn code(self) -> &'static str {
        match self {
            Lang::En => "en",
            Lang::El => "el",
            Lang::Tr => "tr",
            Lang::Zh => "zh",
            Lang::Ja => "ja",
            Lang::Es => "es",
            Lang::Fr => "fr",
            Lang::Th => "th",
            Lang::Ru => "ru",
        }
    }
}

